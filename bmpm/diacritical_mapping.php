      function isASCII($str) {
        return !preg_match('/^[\x00-\x7f]/', $str);
      }

      function Map($str) {
        global $trans;
        if (!isASCII($str)) {
          for ($i = 0; $i < count($trans); $i++) {
            $str = str_replace($trans[$i][0], $trans[$i][1], $str);
          }
        }
        return $str;
      }

